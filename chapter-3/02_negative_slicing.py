name = "Supriya"

print(name[0:3])

print(name[-4:-1]) #negative slicing, it starts from the end of the string and goes backwards, -1 is exclusive
print(name[:4]) # is same as print(name[0:4])
print(name[1:4])# is same as print(name[1:len(name)]) or print(name[1:4]) because it will go till the end of the string
